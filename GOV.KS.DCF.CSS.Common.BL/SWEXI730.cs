        /// Method M_NUMERIC_FORMATTER_EXIT
        /// </summary>
        private void M_NUMERIC_FORMATTER_EXIT(string returnMethod = "")
        {
            if (returnMethod == "M_NUMERIC_FORMATTER_EXIT") { return; }                                         //COBOL==> EXIT.
        }
