                            else
                            {
                                WS.EP_FORMATTED_VALUE_X[WS.EP_OUT_INDEX.AsInt()].SetValue(".");                                     //COBOL==> MOVE '.' TO EP-FORMATTED-VALUE-X ( EP-OUT-INDEX )
