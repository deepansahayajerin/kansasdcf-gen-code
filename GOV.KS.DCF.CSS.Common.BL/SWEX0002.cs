                DisplayToLog("SWEX0002: FILE OPEN AT " + WS.WS_RUN_TIME.GetSubstring(1, 2).AsString() + ":" + WS.WS_RUN_TIME.GetSubstring(3, 2).AsString() + ":" + WS.WS_RUN_TIME.GetSubstring(5, 2).AsString());  //COBOL==> DISPLAY 'SWEX0002: FILE OPEN AT ' WS-RUN-TIME ( 1:2 ) ':' WS-RUN-TIME ( 3:2 ) ':' WS-RUN-TIME ( 5:2 )
            }                                                                                                   //COBOL==> ELSE
            else
