        private void RunMain()
        {
            string returnMethod = "Main";
            LS.STATUS_0025.SetValue("OK");                                                                      //COBOL==> MOVE 'OK' TO STATUS-0025.
            WS.VALID_ACTION_SW.SetValue(LS.ACTION_0025);                                                        //COBOL==> MOVE ACTION-0025 TO VALID-ACTION-SW
                                                                                                                // EvaluateList !LS.ACTION_0025!                                                                    //COBOL==> EVALUATE ACTION-0025
            if ((LS.ACTION_0025.IsEqualTo("OPEN")))                                                             //COBOL==> WHEN 'OPEN'
            {
                M_1100_OPEN_FILE(); if (Control.ExitProgram) { return; }                                              //COBOL==> PERFORM 1100-OPEN-FILE
            }                                                                                                //COBOL==> WHEN 'WRITE'
            else
            if ((LS.ACTION_0025.IsEqualTo("WRITE")))
            {
                M_1250_WRITE_RECORD(); if (Control.ExitProgram) { return; }                                           //COBOL==> PERFORM 1250-WRITE-RECORD
            }                                                                                                //COBOL==> WHEN 'CLOSE'
            else
            if ((LS.ACTION_0025.IsEqualTo("CLOSE")))
            {
                M_1300_CLOSE_FILE(); if (Control.ExitProgram) { return; }                                             //COBOL==> PERFORM 1300-CLOSE-FILE
            }                                                                                                //COBOL==> WHEN OTHER
            else
            {
                M_1250_WRITE_RECORD(); if (Control.ExitProgram) { return; }                                           //COBOL==> PERFORM 1250-WRITE-RECORD
            }                                                                                                   //COBOL==> END-EVALUATE.
            Control.ExitProgram = true; return;                                                                 //COBOL==> GOBACK.
        }
        /// <summary>
        /// Method M_1100_OPEN_FILE
        /// </summary>
        private void M_1100_OPEN_FILE(string returnMethod = "")
        {
            FD.OUTDD01.OpenFile(FileAccessMode.Write);                                                          //COBOL==> OPEN OUTPUT OUTDD01
            if (WS.FILE_STS.IsEqualTo(0))                                                                       //COBOL==> IF FILE-STS = 0
