                DisplayToLog("SWEX0002: OUTDD01 WRITE ERROR - " + "FILE STATUS IS " + WS.FILE_STS.AsString());      //COBOL==> DISPLAY 'SWEX0002: OUTDD01 WRITE ERROR - ' 'FILE STATUS IS ' FILE-STS
