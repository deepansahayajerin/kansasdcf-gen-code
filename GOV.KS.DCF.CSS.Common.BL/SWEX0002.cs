            if (WS.FILE_STS.IsEqualTo("00"))                                                                    //COBOL==> IF FILE-STS = '00'
            {
                WS.WS_RUN_TIME.SetValue(ApplicationDate.TimeOfDay.ToString("hhmmssff"));                            //COBOL==> ACCEPT WS-RUN-TIME FROM TIME
                DisplayToLog("                                          ");                                         //COBOL==> DISPLAY '                                          '
                DisplayToLog("==========================================");                                         //COBOL==> DISPLAY '=========================================='
                WS.DISPLAY_COUNTS.SetValue(WS.WS_TOTAL_OUTP_RECORDS);                                               //COBOL==> MOVE WS-TOTAL-OUTP-RECORDS TO DISPLAY-COUNTS
                DisplayToLog("SWEX0002: # RECS WRITTEN   " + WS.DISPLAY_COUNTS.AsString());                         //COBOL==> DISPLAY 'SWEX0002: # RECS WRITTEN   ' DISPLAY-COUNTS
                if (WS.WS_TOTAL_OUTP_RECORDS.IsEqualTo(0))                                                          //COBOL==> IF WS-TOTAL-OUTP-RECORDS = 0
                {
                    DisplayToLog("SWEX0002: EMPTY FILE       ");                                                        //COBOL==> DISPLAY 'SWEX0002: EMPTY FILE       '
                }                                                                                                   //COBOL==> END-IF
                DisplayToLog("==========================================");                                         //COBOL==> DISPLAY '=========================================='
                DisplayToLog("SWEX0002: FILE CLOSED AT " + WS.WS_RUN_TIME.GetSubstring(1, 2).AsString() + ":" + WS.WS_RUN_TIME.GetSubstring(3, 2).AsString() + ":" + WS.WS_RUN_TIME.GetSubstring(5, 2).AsString());  //COBOL==> DISPLAY 'SWEX0002: FILE CLOSED AT ' WS-RUN-TIME ( 1:2 ) ':' WS-RUN-TIME ( 3:2 ) ':' WS-RUN-TIME ( 5:2 )
                WS.FILE_OPEN_SW.SetValue(WS.NOPE);                                                                  //COBOL==> MOVE NOPE TO FILE-OPEN-SW
            }                                                                                                   //COBOL==> ELSE
            else
            {
                DisplayToLog("SWEX0002: OUTDD01 CLOSE ERROR - " + WS.FILE_STS.AsString());                          //COBOL==> DISPLAY 'SWEX0002: OUTDD01 CLOSE ERROR - ' FILE-STS
            }                                                                                                   //COBOL==> END-IF.
        }
        #endregion
    }
