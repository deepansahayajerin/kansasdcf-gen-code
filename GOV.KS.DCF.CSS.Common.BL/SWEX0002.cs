                DisplayToLog("SWEX0002: OPEN ERROR - " + "FILE STATUS IS " + WS.FILE_STS.AsString());               //COBOL==> DISPLAY 'SWEX0002: OPEN ERROR - ' 'FILE STATUS IS ' FILE-STS
                LS.STATUS_0025.SetValue("OPEN ERR");                                                                //COBOL==> MOVE 'OPEN ERR' TO STATUS-0025
            }                                                                                                   //COBOL==> END-IF.
            if (returnMethod != "" && returnMethod != "M_1100_OPEN_FILE") { M_1250_WRITE_RECORD(returnMethod); }  //Check for pass through to next method
        }
        /// <summary>
        /// Method M_1250_WRITE_RECORD
        /// </summary>
        private void M_1250_WRITE_RECORD(string returnMethod = "")
        {
            if (WS.FILE_NOT_OPEN.Value)                                                                         //COBOL==> IF FILE-NOT-OPEN
