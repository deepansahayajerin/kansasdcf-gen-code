            FD.OUTDD01.WriteLine(LS.W_IA.AsBytes);                                                              //COBOL==> WRITE OUT-RECORD FROM W-IA
            if (WS.FILE_STS.IsEqualTo(0))                                                                       //COBOL==> IF FILE-STS = 0
            {
