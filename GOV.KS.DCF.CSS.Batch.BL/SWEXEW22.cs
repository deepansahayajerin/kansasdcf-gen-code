                    WS.NO_MORE_RECORDS_SWITCH.SetValue("Y");                                                            //COBOL==> MOVE 'Y' TO NO-MORE-RECORDS-SWITCH
                }                                                                                                   //COBOL==> END-READ
                if ((!(WS.FILE_STATUS.IsEqualTo("00")))
             && (WS.NO_MORE_RECORDS_SWITCH.IsEqualTo("N")))  //COBOL==> IF ( FILE-STATUS NOT = '00' AND NO-MORE-RECORDS-SWITCH = 'N' )
                {
                    LS.STATUS_0031.SetValue("R2");                                                                      //COBOL==> MOVE 'R2' TO STATUS-0031
                    M_9999_FINISHED(CheckGotoReturn(returnMethod)); return;                                             //COBOL==> GO TO 9999-FINISHED
                }                                                                                                   //COBOL==> END-IF
            }                                                                                                   //COBOL==> END-PERFORM
            M_4000_CLOSE_FILE(); if (Control.ExitProgram) { return; }                                             //COBOL==> PERFORM 4000-CLOSE-FILE
            M_1100_EXTEND_FILE(); if (Control.ExitProgram) { return; }                                            //COBOL==> PERFORM 1100-EXTEND-FILE.
            if (returnMethod != "" && returnMethod != "M_3000_COMMIT_FILE") { M_4000_CLOSE_FILE(returnMethod); }  //Check for pass through to next method
        }
